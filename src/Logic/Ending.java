package Logic;

public class Ending {
    private String good;
    private String bad;
    private String preEnding;
    private String prologue;

    public Ending() {
    }

    public String getGood() {
        return good;
    }
    public String getBad() {
        return bad;
    }
    public String getPreEnding() {
        return preEnding;
    }
    public String getPrologue() {
        return prologue;
    }
}
